class Solution{
  public:
  
    //Function to find starting point where the truck can start to get through
    //the complete circle without exhausting its petrol in between.
    int tour(petrolPump p[],int n)
    {
        int excess=0,stidx=0,deficit=0;
        for(int i=0;i<n;i++){
            excess+=(p[i].petrol-p[i].distance);
            if(excess<0){
                deficit+=excess;
                excess=0;
                stidx=i+1;
            }
        }
        return excess+deficit>=0 ? stidx: -1;
       //Your code here
    }
};



